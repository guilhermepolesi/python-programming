# Your task is to complete the code in order to evaluate the results of four basic arithmetic operations.
# The results must be printed to the console.

value_a = float(input("Value for variable a: "))
value_b = float(input("Value for variable b: "))

print("The result of addition: " + str(value_a + value_b))
print("The result of subtraction: " + str(value_a - value_b))
print("The result of multiplication: " + str(value_a * value_b))
print("The result of division: " + str(value_a / value_b))

print("\nThat's all, folks!")